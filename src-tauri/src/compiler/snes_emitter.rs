fn tile_base_lookup(ast: &AstOutput) -> std::collections::HashMap<String, u16> {
    let mut lookup = std::collections::HashMap::new();
    let mut accumulated_tiles = 0u16;

    for asset in &ast.sprite_assets {
        lookup.insert(asset.resource_name.clone(), accumulated_tiles);
        accumulated_tiles = accumulated_tiles.saturating_add(tile_count(asset));
    }

    lookup
}

fn build_resource_manifest(ast: &AstOutput) -> String {
    let mut out = String::new();
    let tilemap_assets = collect_tilemap_assets(ast);
    let sfx_resources = collect_sfx_resources(ast);
    let bgm_tracks = collect_bgm_tracks(ast);
    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str("// Workspace SNES asset manifest\n\n");

    if ast.sprite_assets.is_empty()
        && tilemap_assets.is_empty()
        && sfx_resources.is_empty()
        && bgm_tracks.is_empty()
    {
        out.push_str("# no visual assets staged for this target\n");
        return out;
    }

    for asset in &tilemap_assets {
        out.push_str(&format!(
            "BMP {} <- {} ({}x{} tiles)\n",
            asset.resource_name, asset.asset_path, asset.map_width, asset.map_height
        ));
    }

    for asset in &ast.sprite_assets {
        out.push_str(&format!(
            "BMP {} <- {} ({}x{}, pal {})\n",
            asset.resource_name,
            asset.asset_path,
            asset.frame_width,
            asset.frame_height,
            asset.palette_slot
        ));
    }
    for (resource_name, asset_path) in sfx_resources {
        out.push_str(&format!("SFX {} <- {}\n", resource_name, asset_path));
    }
    for (resource_name, asset_path) in bgm_tracks {
        out.push_str(&format!("BGM {} <- {}\n", resource_name, asset_path));
    }

    out
}

fn sprite_size_config(frame_width: u32, frame_height: u32) -> Option<SpriteSizeConfig> {
    if frame_width != frame_height {
        return None;
    }

    match frame_width {
        8 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE8_L16",
            sprite_size: "OBJ_SMALL",
        }),
        16 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE16_L32",
            sprite_size: "OBJ_SMALL",
        }),
        32 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_SMALL",
        }),
        64 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_LARGE",
        }),
        _ => None,
    }
}

fn normalized_animation_frames(frames: &[u32]) -> Vec<u16> {
    if frames.is_empty() {
        vec![0]
    } else {
        frames
            .iter()
            .map(|frame| (*frame).min(u16::MAX as u32) as u16)
            .collect()
    }
}

fn tiles_per_frame(asset: &SpriteAsset) -> u16 {
    let width_tiles = (asset.frame_width / 8).max(1);
    let height_tiles = (asset.frame_height / 8).max(1);
    (width_tiles * height_tiles).min(u16::MAX as u32) as u16
}

fn tile_count(asset: &SpriteAsset) -> u16 {
    let frame_count = asset.animation_count.max(1);
    (u32::from(tiles_per_frame(asset)) * frame_count).min(u16::MAX as u32) as u16
}

fn collect_scroll_tilemap_layers(ast: &AstOutput) -> Vec<String> {
    let mut layers = Vec::new();
    for node in &ast.nodes {
        if let AstNode::ScrollTilemap { layer, .. } = node {
            if !layers.iter().any(|existing| existing == layer) {
                layers.push(layer.clone());
            }
        }
    }
    layers
}

fn collect_camera_scroll_bgs(ast: &AstOutput) -> Vec<u8> {
    let mut bg_numbers = Vec::new();
    let mut next_bg_number = 0u8;

    for node in &ast.nodes {
        if matches!(node, AstNode::DrawTilemap { .. }) {
            let bg_number = next_bg_number.min(2);
            next_bg_number = next_bg_number.saturating_add(1);
            if !bg_numbers.contains(&bg_number) {
                bg_numbers.push(bg_number);
            }
        }
    }

    if !bg_numbers.is_empty() {
        return bg_numbers;
    }

    for layer in collect_scroll_tilemap_layers(ast) {
        let bg_number = snes_bg_number(&layer);
        if !bg_numbers.contains(&bg_number) {
            bg_numbers.push(bg_number);
        }
    }

    if bg_numbers.is_empty() {
        vec![0]
    } else {
        bg_numbers
    }
}

fn snes_bg_number(layer: &str) -> u8 {
    match layer {
        "BG_B" | "BG2" => 1,
        "WINDOW" | "BG3" => 2,
        _ => 0,
    }
}

fn scroll_layer_id(layer: &str) -> String {
    layer
        .chars()
        .map(|character| {
            if character.is_ascii_alphanumeric() || character == '_' {
                character.to_ascii_lowercase()
            } else {
                '_'
            }
        })
        .collect()
}

fn snes_map_size_mode(asset: &TilemapAsset) -> &'static str {
    match (asset.map_width > 32, asset.map_height > 32) {
        (false, false) => "SC_32x32",
        (true, false) => "SC_64x32",
        (false, true) => "SC_32x64",
        (true, true) => "SC_64x64",
    }
}

fn tilemap_tile_address(bg_number: u8) -> u16 {
    0x2000 + (u16::from(bg_number) * 0x1000)
}

fn tilemap_map_address(bg_number: u8) -> u16 {
    u16::from(bg_number) * 0x1000
}

