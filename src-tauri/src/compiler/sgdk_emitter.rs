fn plane_size_for_tilemaps(tilemap_assets: &[TilemapAsset]) -> Option<(u16, u16)> {
    if tilemap_assets.is_empty() {
        return None;
    }

    let max_width = tilemap_assets
        .iter()
        .map(|asset| if asset.map_width > 32 { 64 } else { 32 })
        .max()
        .unwrap_or(32) as u16;
    let max_height = tilemap_assets
        .iter()
        .map(|asset| if asset.map_height > 32 { 64 } else { 32 })
        .max()
        .unwrap_or(32) as u16;

    Some((max_width, max_height))
}

/// Sprites that need position tracking (SGDK 2.x: no SPR_getX/SPR_getY).
/// Returns (var_name, initial_x, initial_y). Initial values from SpawnSprite; defaults (0,0) for logic-only refs.
fn collect_sprite_position_vars(ast: &AstOutput) -> Vec<(String, i32, i32)> {
    let mut spawns: std::collections::HashMap<String, (i32, i32)> =
        std::collections::HashMap::new();
    for node in &ast.nodes {
        if let AstNode::SpawnSprite { var_name, x, y, .. } = node {
            spawns.insert(var_name.clone(), (*x, *y));
        }
    }
    let mut needed = std::collections::BTreeSet::new();
    // Qualquer SpawnSprite precisa de variáveis s16 *_x / *_y antes do corpo de main, mesmo que o
    // NodeGraph ainda não referencie o sprite em MoveSprite (ex.: fixture megadrive_dummy + build SGDK real).
    for var_name in spawns.keys() {
        needed.insert(var_name.clone());
    }
    for node in &ast.nodes {
        if let AstNode::MoveCamera { target, .. } = node {
            if target.starts_with("spr_") {
                needed.insert(target.clone());
            }
        }
    }
    for script in &ast.logic_scripts {
        collect_sprite_vars_from_ops(&script.ops, &mut needed);
    }
    for physics in collect_physics_applications(ast) {
        needed.insert(physics.var_name);
    }
    let mut result: Vec<_> = needed
        .into_iter()
        .map(|var_name| {
            let (x, y) = spawns.get(&var_name).copied().unwrap_or((0, 0));
            (var_name, x, y)
        })
        .collect();
    result.sort_by(|a, b| a.0.cmp(&b.0));
    result
}

fn collect_sprite_vars_from_ops(ops: &[LogicOp], out: &mut std::collections::BTreeSet<String>) {
    for op in ops {
        match op {
            LogicOp::MoveSprite { target_var, .. }
            | LogicOp::SetSpritePosition { target_var, .. }
            | LogicOp::SetAnimationState { target_var, .. }
            | LogicOp::CameraFollow { target_var, .. }
            | LogicOp::ShowSprite { target_var, .. }
            | LogicOp::HideSprite { target_var } => {
                out.insert(target_var.clone());
            }
            LogicOp::ConditionOverlap { left, right, .. } => {
                if let LogicPositionSource::SpriteVar { var_name } = &left.position {
                    out.insert(var_name.clone());
                }
                if let LogicPositionSource::SpriteVar { var_name } = &right.position {
                    out.insert(var_name.clone());
                }
            }
            LogicOp::ConditionBool {
                if_true, if_false, ..
            } => {
                collect_sprite_vars_from_ops(if_true, out);
                collect_sprite_vars_from_ops(if_false, out);
            }
            LogicOp::HardwareEvent { ops: inner, .. } => collect_sprite_vars_from_ops(inner, out),
            LogicOp::StateMachine { states, .. } => {
                for state in states {
                    collect_sprite_vars_from_ops(&state.body, out);
                    for t in &state.transitions {
                        collect_sprite_vars_from_ops(&t.if_matched, out);
                        collect_sprite_vars_from_ops(&t.if_unmatched, out);
                    }
                }
            }
            LogicOp::WhileLoop { body, done, .. } => {
                collect_sprite_vars_from_ops(body, out);
                collect_sprite_vars_from_ops(done, out);
            }
            LogicOp::ForLoop { body, done, .. } => {
                collect_sprite_vars_from_ops(body, out);
                collect_sprite_vars_from_ops(done, out);
            }
            LogicOp::TimelineSequence { slots, .. } => {
                for slot in slots {
                    collect_sprite_vars_from_ops(&slot.actions, out);
                }
            }
            LogicOp::HardwareBudgetCheck { if_ok, if_warn, .. } => {
                collect_sprite_vars_from_ops(if_ok, out);
                collect_sprite_vars_from_ops(if_warn, out);
            }
            _ => {}
        }
    }
}

fn collect_scroll_tilemap_layers(ast: &AstOutput) -> Vec<String> {
    let mut layers = Vec::new();
    for node in &ast.nodes {
        if let AstNode::ScrollTilemap { layer, .. } = node {
            if !layers.iter().any(|existing| existing == layer) {
                layers.push(layer.clone());
            }
        }
    }
    layers
}

fn collect_camera_scroll_planes(ast: &AstOutput) -> Vec<&'static str> {
    let mut planes = Vec::new();
    let mut tilemap_index = 0usize;

    for node in &ast.nodes {
        if matches!(node, AstNode::DrawTilemap { .. }) {
            let plane = tilemap_plane(tilemap_index);
            tilemap_index += 1;
            if plane != "WINDOW" && !planes.contains(&plane) {
                planes.push(plane);
            }
        }
    }

    if !planes.is_empty() {
        return if planes.len() >= 2 {
            vec!["BG_B", "BG_A"]
        } else {
            planes
        };
    }

    for layer in collect_scroll_tilemap_layers(ast) {
        let plane = sgdk_scroll_plane(&layer);
        if plane != "WINDOW" && !planes.contains(&plane) {
            planes.push(plane);
        }
    }

    if planes.is_empty() {
        vec!["BG_A"]
    } else if planes.len() >= 2 {
        vec!["BG_B", "BG_A"]
    } else {
        planes
    }
}

fn tilemap_plane(index: usize) -> &'static str {
    match index {
        0 => "BG_B",
        1 => "BG_A",
        _ => "WINDOW",
    }
}

fn sgdk_scroll_plane(layer: &str) -> &'static str {
    match layer {
        "BG_B" => "BG_B",
        "WINDOW" => "WINDOW",
        _ => "BG_A",
    }
}

fn scroll_layer_id(layer: &str) -> String {
    layer
        .chars()
        .map(|character| {
            if character.is_ascii_alphanumeric() || character == '_' {
                character.to_ascii_lowercase()
            } else {
                '_'
            }
        })
        .collect()
}

fn tilemap_base_expression(resource_name: &str, tilemap_assets: &[TilemapAsset]) -> String {
    let mut terms = vec!["TILE_USER_INDEX".to_string()];
    for asset in tilemap_assets {
        if asset.resource_name == resource_name {
            break;
        }
        terms.push(format!("{}.tileset->numTile", asset.resource_name));
    }
    terms.join(" + ")
}

fn tilemap_resource_path(asset_path: &str) -> String {
    let path = std::path::Path::new(asset_path);
    normalize_path(&path.with_extension("bmp").to_string_lossy())
}

fn sprite_resource_path(asset_path: &str) -> String {
    let path = std::path::Path::new(asset_path);
    normalize_path(&path.with_extension("bmp").to_string_lossy())
}

fn normalize_path(path: &str) -> String {
    path.replace('\\', "/")
}

