        assert!(output
            .main_c
            .contains("XGM_startPlayPCM(SFX_WIN, 1, SOUND_PCM_CH_AUTO);"));
        assert!(output.main_c.contains("} else {"));
        assert!(output
            .main_c
            .contains("XGM_startPlayPCM(SFX_LOSE, 1, SOUND_PCM_CH_AUTO);"));
