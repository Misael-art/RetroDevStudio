use crate::compiler::ast_generator::{AstNode, AstOutput, SpriteAsset};

#[derive(Debug)]
pub struct EmitOutput {
    pub main_c: String,
    pub resources_res: String,
}

#[derive(Debug, Clone, Copy)]
struct SpriteSizeConfig {
    oam_size: &'static str,
    sprite_size: &'static str,
}

#[derive(Debug, Clone)]
struct SpawnedSprite {
    var_name: String,
    sprite_index: u16,
    x: i32,
    y: i32,
    priority: u8,
    tile_base: u16,
    palette_slot: u8,
}

#[derive(Debug, Clone)]
struct AnimationState {
    state_name: String,
    sprite_index: u16,
    x: i32,
    y: i32,
    priority: u8,
    palette_slot: u8,
    frame_tiles: Vec<u16>,
    frame_time: u32,
    looping: bool,
}

#[derive(Debug, Default)]
struct SnesContext {
    spawns: Vec<SpawnedSprite>,
    animations: Vec<AnimationState>,
}

pub fn emit_snes(ast: &AstOutput, project_name: &str) -> EmitOutput {
    EmitOutput {
        main_c: build_main_c(ast, project_name),
        resources_res: build_resource_manifest(ast),
    }
}

fn build_main_c(ast: &AstOutput, project_name: &str) -> String {
    let mut out = String::new();
    let size_config = ast
        .sprite_assets
        .first()
        .and_then(|asset| sprite_size_config(asset.frame_width, asset.frame_height))
        .unwrap_or(SpriteSizeConfig {
            oam_size: "OBJ_SIZE16_L32",
            sprite_size: "OBJ_SMALL",
        });
    let tile_base_lookup = tile_base_lookup(ast);
    let context = build_context(ast, &tile_base_lookup);

    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str(&format!("// Project: {}\n", project_name));
    out.push_str("// Target: SNES (PVSnesLib)\n\n");
    out.push_str("#include <snes.h>\n");
    for asset in &ast.sprite_assets {
        out.push_str(&format!("#include \"{}.inc\"\n", asset.resource_name));
    }
    out.push('\n');

    for animation in &context.animations {
        let frame_tiles = animation
            .frame_tiles
            .iter()
            .map(u16::to_string)
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!(
            "static const u16 {}_tiles[] = {{{}}};\n",
            animation.state_name, frame_tiles
        ));
        out.push_str(&format!("static u16 {}_cursor = 0;\n", animation.state_name));
        out.push_str(&format!("static u16 {}_timer = 0;\n", animation.state_name));
    }
    if !context.animations.is_empty() {
        out.push('\n');
    }

    out.push_str("int main(void)\n{\n");
    out.push_str("    consoleInit();\n");
    out.push_str("    oamInit();\n");

    let mut tile_base = 0u16;
    for asset in &ast.sprite_assets {
        let vram_address = tile_base.saturating_mul(16);
        out.push_str(&format!(
            "    oamInitGfxSet(&{name}_til, (&{name}_tilend - &{name}_til), &{name}_pal, (&{name}_palend - &{name}_pal), {palette}, 0x{address:04X}, {oam_size});\n",
            name = asset.resource_name,
            palette = asset.palette_slot,
            address = vram_address,
            oam_size = size_config.oam_size
        ));
        tile_base = tile_base.saturating_add(tile_count(asset));
    }

    for spawn in &context.spawns {
        out.push_str(&format!(
            "    oamSet({index}, {x}, {y}, {priority}, 0, 0, {tile}, {palette});\n",
            index = spawn.sprite_index,
            x = spawn.x.max(0),
            y = spawn.y.max(0),
            priority = spawn.priority,
            tile = initial_tile_for_spawn(&context, &spawn.var_name).unwrap_or(spawn.tile_base),
            palette = spawn.palette_slot
        ));
        out.push_str(&format!(
            "    oamSetEx({}, {}, OBJ_SHOW);\n",
            spawn.sprite_index, size_config.sprite_size
        ));
    }

    for node in &ast.nodes {
        match node {
            AstNode::SpriteSystemInit
            | AstNode::LoadSpritesheet { .. }
            | AstNode::SpawnSprite { .. }
            | AstNode::SetAnimation { .. } => {}
            AstNode::DrawText { x, y, text, .. } => {
                let escaped = text.replace('"', "\\\"");
                out.push_str(&format!(
                    "    consoleDrawText({}, {}, \"{}\");\n",
                    x, y, escaped
                ));
            }
            AstNode::GameLoopBegin => {
                out.push_str("    setMode(BG_MODE1, 0);\n");
                out.push_str("    bgSetDisable(0);\n");
                out.push_str("    bgSetDisable(1);\n");
                out.push_str("    bgSetDisable(2);\n");
                out.push_str("    setScreenOn();\n\n");
                out.push_str("    while (1)\n    {\n");
            }
            AstNode::SpriteUpdate => {
                for animation in &context.animations {
                    render_animation_update(&mut out, animation);
                }
                out.push_str("        oamUpdate();\n");
            }
            AstNode::VSync => {
                out.push_str("        WaitForVBlank();\n");
            }
            AstNode::GameLoopEnd => {
                out.push_str("    }\n");
            }
        }
    }

    out.push_str("\n    return 0;\n}\n");
    out
}

fn build_context(
    ast: &AstOutput,
    tile_base_lookup: &std::collections::HashMap<String, u16>,
) -> SnesContext {
    let mut context = SnesContext::default();
    let mut spawn_lookup: std::collections::HashMap<String, usize> = std::collections::HashMap::new();
    let mut next_sprite_index = 0u16;

    for node in &ast.nodes {
        match node {
            AstNode::SpawnSprite {
                var_name,
                resource_name,
                x,
                y,
                priority_high,
            } => {
                let palette_slot = ast
                    .sprite_assets
                    .iter()
                    .find(|asset| asset.resource_name == *resource_name)
                    .map(|asset| asset.palette_slot)
                    .unwrap_or_default();
                let spawn = SpawnedSprite {
                    var_name: var_name.clone(),
                    sprite_index: next_sprite_index,
                    x: *x,
                    y: *y,
                    priority: if *priority_high { 3 } else { 0 },
                    tile_base: tile_base_lookup
                        .get(resource_name)
                        .copied()
                        .unwrap_or_default(),
                    palette_slot,
                };
                spawn_lookup.insert(var_name.clone(), context.spawns.len());
                context.spawns.push(spawn);
                next_sprite_index = next_sprite_index.saturating_add(1);
            }
            AstNode::SetAnimation {
                var_name,
                resource_name,
                frame_time,
                frames,
                looping,
                ..
            } => {
                let Some(spawn_index) = spawn_lookup.get(var_name).copied() else {
                    continue;
                };
                let Some(asset) = ast
                    .sprite_assets
                    .iter()
                    .find(|asset| asset.resource_name == *resource_name)
                else {
                    continue;
                };
                let spawn = &context.spawns[spawn_index];
                let tiles_per_frame = tiles_per_frame(asset);
                let normalized_frames = if frames.is_empty() {
                    vec![0]
                } else {
                    frames.clone()
                };
                let frame_tiles = normalized_frames
                    .into_iter()
                    .map(|frame| {
                        spawn
                            .tile_base
                            .saturating_add((frame as u16).saturating_mul(tiles_per_frame))
                    })
                    .collect::<Vec<_>>();

                context.animations.push(AnimationState {
                    state_name: format!("{}_anim", var_name),
                    sprite_index: spawn.sprite_index,
                    x: spawn.x,
                    y: spawn.y,
                    priority: spawn.priority,
                    palette_slot: spawn.palette_slot,
                    frame_tiles,
                    frame_time: *frame_time,
                    looping: *looping,
                });
            }
            _ => {}
        }
    }

    context
}

fn render_animation_update(out: &mut String, animation: &AnimationState) {
    if animation.frame_time == 0 || animation.frame_tiles.len() <= 1 {
        return;
    }

    let last_frame = animation.frame_tiles.len() - 1;
    out.push_str(&format!(
        "        if (++{name}_timer >= {delay}) {{\n",
        name = animation.state_name,
        delay = animation.frame_time
    ));
    out.push_str(&format!("            {}_timer = 0;\n", animation.state_name));
    out.push_str(&format!(
        "            if ({}_cursor < {}) {{\n",
        animation.state_name, last_frame
    ));
    out.push_str(&format!("                {}_cursor++;\n", animation.state_name));
    out.push_str(&format!(
        "                oamSet({index}, {x}, {y}, {priority}, 0, 0, {name}_tiles[{name}_cursor], {palette});\n",
        index = animation.sprite_index,
        x = animation.x.max(0),
        y = animation.y.max(0),
        priority = animation.priority,
        name = animation.state_name,
        palette = animation.palette_slot
    ));
    out.push_str("            }");
    if animation.looping {
        out.push_str(" else {\n");
        out.push_str(&format!("                {}_cursor = 0;\n", animation.state_name));
        out.push_str(&format!(
            "                oamSet({index}, {x}, {y}, {priority}, 0, 0, {name}_tiles[{name}_cursor], {palette});\n",
            index = animation.sprite_index,
            x = animation.x.max(0),
            y = animation.y.max(0),
            priority = animation.priority,
            name = animation.state_name,
            palette = animation.palette_slot
        ));
        out.push_str("            }\n");
    } else {
        out.push('\n');
    }
    out.push_str("        }\n");
}

fn initial_tile_for_spawn(context: &SnesContext, var_name: &str) -> Option<u16> {
    context
        .animations
        .iter()
        .find(|animation| animation.state_name == format!("{}_anim", var_name))
        .and_then(|animation| animation.frame_tiles.first().copied())
}

fn tile_base_lookup(ast: &AstOutput) -> std::collections::HashMap<String, u16> {
    let mut lookup = std::collections::HashMap::new();
    let mut accumulated_tiles = 0u16;

    for asset in &ast.sprite_assets {
        lookup.insert(asset.resource_name.clone(), accumulated_tiles);
        accumulated_tiles = accumulated_tiles.saturating_add(tile_count(asset));
    }

    lookup
}

fn build_resource_manifest(ast: &AstOutput) -> String {
    let mut out = String::new();
    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str("// Workspace SNES asset manifest\n\n");

    if ast.sprite_assets.is_empty() {
        out.push_str("# no sprite assets staged for this target\n");
        return out;
    }

    for asset in &ast.sprite_assets {
        out.push_str(&format!(
            "BMP {} <- {} ({}x{}, pal {})\n",
            asset.resource_name,
            asset.asset_path,
            asset.frame_width,
            asset.frame_height,
            asset.palette_slot
        ));
    }

    out
}

fn sprite_size_config(frame_width: u32, frame_height: u32) -> Option<SpriteSizeConfig> {
    if frame_width != frame_height {
        return None;
    }

    match frame_width {
        8 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE8_L16",
            sprite_size: "OBJ_SMALL",
        }),
        16 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE16_L32",
            sprite_size: "OBJ_SMALL",
        }),
        32 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_SMALL",
        }),
        64 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_LARGE",
        }),
        _ => None,
    }
}

fn tiles_per_frame(asset: &SpriteAsset) -> u16 {
    let width_tiles = (asset.frame_width / 8).max(1);
    let height_tiles = (asset.frame_height / 8).max(1);
    (width_tiles * height_tiles).min(u16::MAX as u32) as u16
}

fn tile_count(asset: &SpriteAsset) -> u16 {
    let frame_count = asset.animation_count.max(1);
    (u32::from(tiles_per_frame(asset)) * frame_count).min(u16::MAX as u32) as u16
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::compiler::ast_generator::SpriteAnimation;

    fn sprite_asset() -> SpriteAsset {
        SpriteAsset {
            resource_name: "hero".to_string(),
            asset_path: "assets/sprites/hero.png".to_string(),
            frame_width: 16,
            frame_height: 16,
            palette_slot: 0,
            animation_count: 3,
            default_animation: Some(SpriteAnimation {
                name: "walk".to_string(),
                frames: vec![0, 1, 2],
                frame_time: 6,
                looping: true,
            }),
        }
    }

    #[test]
    fn snes_emitter_generates_frame_timer_for_default_animation() {
        let ast = AstOutput {
            nodes: vec![
                AstNode::SpriteSystemInit,
                AstNode::LoadSpritesheet {
                    resource_name: "hero".to_string(),
                    asset_path: "assets/sprites/hero.png".to_string(),
                    frame_width: 16,
                    frame_height: 16,
                    palette_slot: 0,
                },
                AstNode::SpawnSprite {
                    var_name: "spr_hero".to_string(),
                    resource_name: "hero".to_string(),
                    x: 32,
                    y: 48,
                    priority_high: true,
                },
                AstNode::SetAnimation {
                    var_name: "spr_hero".to_string(),
                    resource_name: "hero".to_string(),
                    anim_index: 0,
                    frame_time: 6,
                    frames: vec![0, 1, 2],
                    looping: true,
                },
                AstNode::GameLoopBegin,
                AstNode::SpriteUpdate,
                AstNode::VSync,
                AstNode::GameLoopEnd,
            ],
            sprite_assets: vec![sprite_asset()],
        };

        let output = emit_snes(&ast, "Timing Demo");

        assert!(output
            .main_c
            .contains("static const u16 spr_hero_anim_tiles[] = {0, 4, 8};"));
        assert!(output.main_c.contains("if (++spr_hero_anim_timer >= 6)"));
        assert!(output
            .main_c
            .contains("oamSet(0, 32, 48, 3, 0, 0, spr_hero_anim_tiles[spr_hero_anim_cursor], 0);"));
    }
}
