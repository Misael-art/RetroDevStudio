use crate::compiler::ast_generator::{AstNode, AstOutput, SpriteAsset};

#[derive(Debug)]
pub struct EmitOutput {
    pub main_c: String,
    pub resources_res: String,
}

#[derive(Debug, Clone, Copy)]
struct SpriteSizeConfig {
    oam_size: &'static str,
    sprite_size: &'static str,
}

pub fn emit_snes(ast: &AstOutput, project_name: &str) -> EmitOutput {
    let main_c = build_main_c(ast, project_name);
    let resources_res = build_resource_manifest(ast);
    EmitOutput {
        main_c,
        resources_res,
    }
}

fn build_main_c(ast: &AstOutput, project_name: &str) -> String {
    let mut out = String::new();
    let size_config = ast
        .sprite_assets
        .first()
        .and_then(|asset| sprite_size_config(asset.frame_width, asset.frame_height))
        .unwrap_or(SpriteSizeConfig {
            oam_size: "OBJ_SIZE16_L32",
            sprite_size: "OBJ_SMALL",
        });

    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str(&format!("// Project: {}\n", project_name));
    out.push_str("// Target: SNES (PVSnesLib)\n\n");
    out.push_str("#include <snes.h>\n");
    for asset in &ast.sprite_assets {
        out.push_str(&format!("#include \"{}.inc\"\n", asset.resource_name));
    }
    out.push('\n');

    out.push_str("int main(void)\n{\n");
    out.push_str("    consoleInit();\n");
    out.push_str("    oamInit();\n");

    let mut tile_base = 0u16;
    for asset in &ast.sprite_assets {
        let vram_address = tile_base.saturating_mul(16);
        out.push_str(&format!(
            "    oamInitGfxSet(&{name}_til, (&{name}_tilend - &{name}_til), &{name}_pal, (&{name}_palend - &{name}_pal), {palette}, 0x{address:04X}, {oam_size});\n",
            name = asset.resource_name,
            palette = asset.palette_slot,
            address = vram_address,
            oam_size = size_config.oam_size
        ));
        tile_base = tile_base.saturating_add(tile_count(asset));
    }

    let mut next_sprite_index = 0u16;
    let mut tile_base_lookup = std::collections::HashMap::new();
    let mut accumulated_tiles = 0u16;
    for asset in &ast.sprite_assets {
        tile_base_lookup.insert(asset.resource_name.clone(), accumulated_tiles);
        accumulated_tiles = accumulated_tiles.saturating_add(tile_count(asset));
    }

    for node in &ast.nodes {
        match node {
            AstNode::SpriteSystemInit | AstNode::LoadSpritesheet { .. } => {}
            AstNode::SpawnSprite {
                resource_name,
                x,
                y,
                priority_high,
                ..
            } => {
                let priority = if *priority_high { 3 } else { 0 };
                let tile_base = tile_base_lookup
                    .get(resource_name)
                    .copied()
                    .unwrap_or_default();
                let palette_slot = ast
                    .sprite_assets
                    .iter()
                    .find(|asset| asset.resource_name == *resource_name)
                    .map(|asset| asset.palette_slot)
                    .unwrap_or_default();
                out.push_str(&format!(
                    "    oamSet({index}, {x}, {y}, {priority}, 0, 0, {tile_base}, {palette});\n",
                    index = next_sprite_index,
                    x = (*x).max(0),
                    y = (*y).max(0),
                    priority = priority,
                    tile_base = tile_base,
                    palette = palette_slot
                ));
                out.push_str(&format!(
                    "    oamSetEx({}, {}, OBJ_SHOW);\n",
                    next_sprite_index, size_config.sprite_size
                ));
                next_sprite_index = next_sprite_index.saturating_add(1);
            }
            AstNode::SetAnimation { var_name, anim_index } => {
                out.push_str(&format!(
                    "    // TODO: animation {} for '{}' requires metasprite/frame stepping support.\n",
                    anim_index, var_name
                ));
            }
            AstNode::DrawText { x, y, text, .. } => {
                let escaped = text.replace('"', "\\\"");
                out.push_str(&format!(
                    "    consoleDrawText({}, {}, \"{}\");\n",
                    x, y, escaped
                ));
            }
            AstNode::GameLoopBegin => {
                out.push_str("    setMode(BG_MODE1, 0);\n");
                out.push_str("    bgSetDisable(0);\n");
                out.push_str("    bgSetDisable(1);\n");
                out.push_str("    bgSetDisable(2);\n");
                out.push_str("    setScreenOn();\n\n");
                out.push_str("    while (1)\n    {\n");
            }
            AstNode::SpriteUpdate => {
                out.push_str("        oamUpdate();\n");
            }
            AstNode::VSync => {
                out.push_str("        WaitForVBlank();\n");
            }
            AstNode::GameLoopEnd => {
                out.push_str("    }\n");
            }
        }
    }

    out.push_str("\n    return 0;\n}\n");
    out
}

fn build_resource_manifest(ast: &AstOutput) -> String {
    let mut out = String::new();
    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str("// Workspace SNES asset manifest\n\n");

    if ast.sprite_assets.is_empty() {
        out.push_str("# no sprite assets staged for this target\n");
        return out;
    }

    for asset in &ast.sprite_assets {
        out.push_str(&format!(
            "BMP {} <- {} ({}x{}, pal {})\n",
            asset.resource_name,
            asset.asset_path,
            asset.frame_width,
            asset.frame_height,
            asset.palette_slot
        ));
    }

    out
}

fn sprite_size_config(frame_width: u32, frame_height: u32) -> Option<SpriteSizeConfig> {
    if frame_width != frame_height {
        return None;
    }

    match frame_width {
        8 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE8_L16",
            sprite_size: "OBJ_SMALL",
        }),
        16 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE16_L32",
            sprite_size: "OBJ_SMALL",
        }),
        32 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_SMALL",
        }),
        64 => Some(SpriteSizeConfig {
            oam_size: "OBJ_SIZE32_L64",
            sprite_size: "OBJ_LARGE",
        }),
        _ => None,
    }
}

fn tile_count(asset: &SpriteAsset) -> u16 {
    let tiles_per_frame = (asset.frame_width / 8).max(1) * (asset.frame_height / 8).max(1);
    let frame_count = asset.animation_count.max(1);
    (tiles_per_frame * frame_count).min(u16::MAX as u32) as u16
}
