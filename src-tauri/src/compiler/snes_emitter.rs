#[derive(Debug)]
pub struct EmitOutput {
    pub main_c: String,
    pub resources_res: String,
}

#[derive(Debug, Clone)]
struct InputCommandSpec {
    id: String,
    notation: String,
    unsupported_tokens: Vec<String>,
}

#[derive(Debug, Clone, Copy)]
struct SpriteSizeConfig {
    oam_size: &'static str,
    sprite_size: &'static str,
}

#[derive(Debug, Clone)]
struct SpawnedSprite {
    var_name: String,
    resource_name: String,
    sprite_index: u16,
    initial_x: i32,
    initial_y: i32,
    priority: u8,
    tile_base: u16,
    palette_slot: u8,
}

#[derive(Debug, Clone)]
struct AnimationState {
    var_name: String,
    state_name: String,
    sprite_index: u16,
    x_var: String,
    y_var: String,
    priority: u8,
    palette_slot: u8,
    frame_tiles: Vec<u16>,
    frame_time: u32,
    looping: bool,
}

#[derive(Debug, Clone)]
struct TilemapDraw {
    resource_name: String,
    bg_number: u8,
    scroll_x: i32,
    scroll_y: i32,
}

#[derive(Debug, Default)]
struct SnesContext {
    spawns: Vec<SpawnedSprite>,
    animations: Vec<AnimationState>,
    tilemaps: Vec<TilemapDraw>,
}

#[allow(dead_code)] // Used in tests; production code uses emit_snes_with_collision
pub fn emit_snes(ast: &AstOutput, project_name: &str) -> EmitOutput {
    emit_snes_with_collision(ast, project_name, None)
}

/// Gera código C (PVSnesLib) a partir do AST + dados opcionais do collision map.
/// Emite `rds_collision_map[]` como array estático se `collision_map_data` for Some.
pub fn emit_snes_with_collision(
    ast: &AstOutput,
    project_name: &str,
    collision_map_data: Option<&[u8]>,
) -> EmitOutput {
    EmitOutput {
        main_c: build_main_c_with_collision(ast, project_name, collision_map_data),
        resources_res: build_resource_manifest(ast),
    }
}

fn build_main_c_with_collision(
    ast: &AstOutput,
    project_name: &str,
    collision_map_data: Option<&[u8]>,
) -> String {
    let mut out = String::new();
    let collision_checks = collect_collision_checks(ast);
    let physics_applications = collect_physics_applications(ast);
    let tilemap_assets = collect_tilemap_assets(ast);
    let parallax_layers = collect_parallax_layers(ast);
    let raster_lines = collect_raster_lines(ast);
    let scroll_tilemap_layers = collect_scroll_tilemap_layers(ast);
    let logic_vars = collect_logic_var_names(ast);
    let sfx_resources = collect_sfx_resources(ast);
    let bgm_tracks = collect_bgm_tracks(ast);
    let has_logic_overlap = ast.logic_scripts.iter().any(script_uses_overlap);
    let input_commands = collect_input_commands(ast);
    let hardware_event_scripts = collect_hardware_event_scripts(ast);
    let default_size_config = SpriteSizeConfig {
        oam_size: "OBJ_SIZE16_L32",
        sprite_size: "OBJ_SMALL",
    };
    let asset_size_configs: std::collections::HashMap<String, SpriteSizeConfig> = ast
        .sprite_assets
        .iter()
        .map(|asset| {
            let config = sprite_size_config(asset.frame_width, asset.frame_height)
                .unwrap_or(default_size_config);
            (asset.resource_name.clone(), config)
        })
        .collect();
    let tile_base_lookup = tile_base_lookup(ast);
    let context = build_context(ast, &tile_base_lookup);

    out.push_str("// Generated by RetroDev Studio - DO NOT EDIT\n");
    out.push_str(&format!("// Project: {}\n", project_name));
    out.push_str("// Target: SNES (PVSnesLib)\n\n");
    out.push_str("#include <snes.h>\n");
    for asset in &tilemap_assets {
        out.push_str(&format!("#include \"{}.inc\"\n", asset.resource_name));
    }
    for asset in &ast.sprite_assets {
        out.push_str(&format!("#include \"{}.inc\"\n", asset.resource_name));
    }
    out.push('\n');
    render_sound_id_macros(&mut out, ast);
    render_audio_externs(&mut out, &sfx_resources, &bgm_tracks);

    for animation in &context.animations {
        let frame_tiles = animation
            .frame_tiles
            .iter()
            .map(u16::to_string)
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!(
            "static const u16 {}_tiles[] = {{{}}};\n",
            animation.state_name, frame_tiles
        ));
        out.push_str(&format!(
            "static u16 {}_cursor = 0;\n",
            animation.state_name
        ));
        out.push_str(&format!("static u16 {}_timer = 0;\n", animation.state_name));
    }
    if !context.animations.is_empty() {
        out.push('\n');
    }
    for spawn in &context.spawns {
        out.push_str(&format!(
            "static s16 {}_x = {};\n",
            spawn.var_name, spawn.initial_x
        ));
        out.push_str(&format!(
            "static s16 {}_y = {};\n",
            spawn.var_name, spawn.initial_y
        ));
    }
    if !context.spawns.is_empty() {
        out.push('\n');
    }
    for physics in &physics_applications {
        out.push_str(&format!("static s32 {}_vel_x = 0;\n", physics.var_name));
        out.push_str(&format!("static s32 {}_vel_y = 0;\n", physics.var_name));
    }
    if !physics_applications.is_empty() {
        out.push('\n');
    }
    for layer in &scroll_tilemap_layers {
        let layer_id = scroll_layer_id(layer);
        out.push_str(&format!("static s16 tm_scroll_x_{} = 0;\n", layer_id));
        out.push_str(&format!("static s16 tm_scroll_y_{} = 0;\n", layer_id));
    }
    if !scroll_tilemap_layers.is_empty() {
        out.push('\n');
    }
    for var_name in &logic_vars {
        out.push_str(&format!("static s32 logic_var_{} = 0;\n", var_name));
    }
    if !logic_vars.is_empty() {
        out.push('\n');
    }
    if !parallax_layers.is_empty() || !raster_lines.is_empty() {
        for (index, _) in parallax_layers.iter().enumerate() {
            out.push_str(&format!(
                "static s16 retro_parallax_offset_{}_x = 0;\n",
                index
            ));
            out.push_str(&format!(
                "static s16 retro_parallax_offset_{}_y = 0;\n",
                index
            ));
        }
        out.push('\n');
    }
    if !collision_checks.is_empty() || has_logic_overlap {
        out.push_str(render_aabb_helper());
        out.push('\n');
    }
    if !input_commands.is_empty() {
        render_input_command_runtime(&mut out, &input_commands);
        out.push('\n');
    }
    if !hardware_event_scripts.is_empty() {
        render_snes_hardware_event_handlers(&mut out, &context, &hardware_event_scripts);
        out.push('\n');
    }

    // ── Collision map array (schema 1.4.0+) ────────────────────────────────────
    if let Some(data) = collision_map_data {
        if !data.is_empty() {
            out.push_str("// Collision map generated by RetroDev Studio (0=free, 1=solid)\n");
            out.push_str(&format!(
                "static const u8 rds_collision_map[{}] = {{\n    ",
                data.len()
            ));
            let entries: Vec<String> = data.iter().map(|b| b.to_string()).collect();
            out.push_str(&entries.join(", "));
            out.push_str("\n};\n\n");
        }
    }

    out.push_str("int main(void)\n{\n");
    out.push_str("    consoleInit();\n");
    out.push_str("    oamInit();\n");

    for tilemap in &context.tilemaps {
        let Some(asset) = tilemap_assets
            .iter()
            .find(|asset| asset.resource_name == tilemap.resource_name)
        else {
            continue;
        };
        out.push_str(&format!(
            "    bgInitTileSet({bg}, &{name}_til, &{name}_pal, 0, (&{name}_tilend - &{name}_til), (&{name}_palend - &{name}_pal), BG_16COLORS, 0x{tile_addr:04X});\n",
            bg = tilemap.bg_number,
            name = tilemap.resource_name,
            tile_addr = tilemap_tile_address(tilemap.bg_number)
        ));
        out.push_str(&format!(
            "    bgInitMapSet({bg}, (u8*)&{name}_map, (&{name}_mapend - &{name}_map), {size_mode}, 0x{map_addr:04X});\n",
            bg = tilemap.bg_number,
            name = tilemap.resource_name,
            size_mode = snes_map_size_mode(asset),
            map_addr = tilemap_map_address(tilemap.bg_number)
        ));
        if tilemap.scroll_x != 0 || tilemap.scroll_y != 0 {
            out.push_str(&format!(
                "    bgSetScroll({}, {}, {});\n",
                tilemap.bg_number, tilemap.scroll_x, tilemap.scroll_y
            ));
        }
    }

    let mut tile_base = 0u16;
    for asset in &ast.sprite_assets {
        let vram_address = tile_base.saturating_mul(16);
        let asset_config = asset_size_configs
            .get(&asset.resource_name)
            .copied()
            .unwrap_or(default_size_config);
        out.push_str(&format!(
            "    oamInitGfxSet(&{name}_til, (&{name}_tilend - &{name}_til), &{name}_pal, (&{name}_palend - &{name}_pal), {palette}, 0x{address:04X}, {oam_size});\n",
            name = asset.resource_name,
            palette = asset.palette_slot,
            address = vram_address,
            oam_size = asset_config.oam_size
        ));
        tile_base = tile_base.saturating_add(tile_count(asset));
    }

    for spawn in &context.spawns {
        let x_var = format!("{}_x", spawn.var_name);
        let y_var = format!("{}_y", spawn.var_name);
        out.push_str(&format!(
            "    oamSet({index}, {x}, {y}, {priority}, 0, 0, {tile}, {palette});\n",
            index = spawn.sprite_index,
            x = x_var,
            y = y_var,
            priority = spawn.priority,
            tile = initial_tile_for_spawn(&context, &spawn.var_name).unwrap_or(spawn.tile_base),
            palette = spawn.palette_slot
        ));
        let spawn_size_config = asset_size_configs
            .get(&spawn.resource_name)
            .copied()
            .unwrap_or(default_size_config);
        out.push_str(&format!(
            "    oamSetEx({}, {}, OBJ_SHOW);\n",
            spawn.sprite_index, spawn_size_config.sprite_size
        ));
    }

    for (event, _) in &hardware_event_scripts {
        out.push_str(&snes_event_registration(*event));
    }
    for node in &ast.nodes {
        match node {
            AstNode::SpriteSystemInit
            | AstNode::LoadTilemap { .. }
            | AstNode::LoadSpritesheet { .. }
            | AstNode::DrawTilemap { .. }
            | AstNode::SpawnSprite { .. }
            | AstNode::SetAnimation { .. } => {}
            AstNode::ScrollTilemap { layer, dx, dy } => {
                render_scroll_tilemap(&mut out, layer, *dx, *dy);
            }
            AstNode::MoveCamera { target, x, y } => {
                let camera_bgs = collect_camera_scroll_bgs(ast);
                render_move_camera(&mut out, target, *x, *y, &camera_bgs);
            }
            AstNode::SetupParallax { layers } => {
                out.push_str(&format!(
                    "    // RetroFX parallax configured for {} layer(s)\n",
                    layers.len()
                ));
            }
            AstNode::SetupRasterEffect { lines } => {
                out.push_str(&format!(
                    "    // RetroFX raster configured for {} line(s)\n",
                    lines.len()
                ));
            }
            AstNode::InitAudio { sfx_resources } => {
                out.push_str("    spcBoot();\n");
                for (resource_name, _) in sfx_resources {
                    out.push_str(&format!(
                        "    spcSetSoundEntry(SFX_{sound_id}, 8, 0, (u8*)&{symbol});\n",
                        sound_id = resource_name.to_uppercase(),
                        symbol = snes_sfx_symbol(resource_name)
                    ));
                }
            }
            AstNode::PlayBgm { resource_name, .. } => {
                out.push_str(&format!(
                    "    spcLoad((u8*)&{});\n",
                    snes_bgm_symbol(resource_name)
                ));
            }
            AstNode::ReadInputDevice { device, state_var } => {
                out.push_str(&format!(
                    "        u16 {} = padsCurrent({});\n",
                    state_var,
                    snes_joypad_port(device)
                ));
            }
            AstNode::MapInputAction {
                result_name,
                entity_id,
                action_name,
                state_var,
                button,
            } => {
                render_input_binding(
                    &mut out,
                    result_name,
                    entity_id,
                    action_name,
                    state_var,
                    button,
                );
            }
            AstNode::CheckCollisionAabb {
                result_name,
                left,
                right,
            } => {
                render_collision_check(
                    &mut out,
                    &AabbCollisionCheck {
                        result_name: result_name.clone(),
                        left: left.clone(),
                        right: right.clone(),
                    },
                );
            }
            AstNode::ApplyPhysics {
                var_name,
                gravity,
                gravity_strength,
                max_velocity_x,
                max_velocity_y,
                friction,
                bounce,
            } => render_apply_physics(
                &mut out,
                &context,
                &PhysicsApplication {
                    var_name: var_name.clone(),
                    gravity: *gravity,
                    gravity_strength: *gravity_strength,
                    max_velocity_x: *max_velocity_x,
                    max_velocity_y: *max_velocity_y,
                    friction: *friction,
                    bounce: *bounce,
                },
            ),
            AstNode::DrawText { x, y, text, .. } => {
                let escaped = text.replace('"', "\\\"");
                out.push_str(&format!(
                    "    consoleDrawText({}, {}, \"{}\");\n",
                    x, y, escaped
                ));
            }
            AstNode::GameLoopBegin => {
                out.push_str("    setMode(BG_MODE1, 0);\n");
                if context.tilemaps.is_empty() {
                    out.push_str("    bgSetDisable(0);\n");
                    out.push_str("    bgSetDisable(1);\n");
                    out.push_str("    bgSetDisable(2);\n");
                } else {
                    for bg_number in 0..=2 {
                        if !context
                            .tilemaps
                            .iter()
                            .any(|tilemap| tilemap.bg_number == bg_number)
                        {
                            out.push_str(&format!("    bgSetDisable({});\n", bg_number));
                        }
                    }
                }
                out.push_str("    setScreenOn();\n\n");
                out.push_str("    while (1)\n    {\n");
            }
            AstNode::SpriteUpdate => {
                if !input_commands.is_empty() {
                    out.push_str("        rds_input_push_frame(padsCurrent(0));\n");
                }
                render_logic_scripts(&mut out, &ast.logic_scripts, &context, 8);
                render_retrofx_frame(&mut out, &parallax_layers, &raster_lines, 8);
                for animation in &context.animations {
                    render_animation_update(&mut out, animation);
                }
                out.push_str("        oamUpdate();\n");
            }
            AstNode::VSync => {
                out.push_str("        WaitForVBlank();\n");
            }
            AstNode::GameLoopEnd => {
                out.push_str("    }\n");
            }
        }
    }

    out.push_str("\n    return 0;\n}\n");
    out
}

fn build_context(
    ast: &AstOutput,
    tile_base_lookup: &std::collections::HashMap<String, u16>,
) -> SnesContext {
    let mut context = SnesContext::default();
    let mut spawn_lookup: std::collections::HashMap<String, usize> =
        std::collections::HashMap::new();
    let mut next_sprite_index = 0u16;
    let mut next_bg_number = 0u8;

    for node in &ast.nodes {
        match node {
            AstNode::DrawTilemap {
                resource_name,
                x,
                y,
                scroll_x,
                scroll_y,
            } => {
                context.tilemaps.push(TilemapDraw {
                    resource_name: resource_name.clone(),
                    bg_number: next_bg_number.min(2),
                    scroll_x: x.saturating_add(*scroll_x),
                    scroll_y: y.saturating_add(*scroll_y),
                });
                next_bg_number = next_bg_number.saturating_add(1);
            }
            AstNode::SpawnSprite {
                var_name,
                resource_name,
                x,
                y,
                priority_high,
            } => {
                let palette_slot = ast
                    .sprite_assets
                    .iter()
                    .find(|asset| asset.resource_name == *resource_name)
                    .map(|asset| asset.palette_slot)
                    .unwrap_or_default();
                let spawn = SpawnedSprite {
                    var_name: var_name.clone(),
                    resource_name: resource_name.clone(),
                    sprite_index: next_sprite_index,
                    initial_x: *x,
                    initial_y: *y,
                    priority: if *priority_high { 3 } else { 0 },
                    tile_base: tile_base_lookup
                        .get(resource_name)
                        .copied()
                        .unwrap_or_default(),
                    palette_slot,
                };
                spawn_lookup.insert(var_name.clone(), context.spawns.len());
                context.spawns.push(spawn);
                next_sprite_index = next_sprite_index.saturating_add(1);
            }
            AstNode::SetAnimation {
                var_name,
                resource_name,
                frame_time,
                frames,
                looping,
                ..
            } => {
                let Some(spawn_index) = spawn_lookup.get(var_name).copied() else {
                    continue;
                };
                let Some(asset) = ast
                    .sprite_assets
                    .iter()
                    .find(|asset| asset.resource_name == *resource_name)
                else {
                    continue;
                };
                let spawn = &context.spawns[spawn_index];
                let frame_tiles = normalized_animation_frames(frames)
                    .into_iter()
                    .map(|frame| {
                        spawn
                            .tile_base
                            .saturating_add(frame.saturating_mul(tiles_per_frame(asset)))
                    })
                    .collect::<Vec<_>>();

                context.animations.push(AnimationState {
                    var_name: var_name.clone(),
                    state_name: format!("{}_anim", var_name),
                    sprite_index: spawn.sprite_index,
                    x_var: format!("{}_x", spawn.var_name),
                    y_var: format!("{}_y", spawn.var_name),
                    priority: spawn.priority,
                    palette_slot: spawn.palette_slot,
                    frame_tiles,
                    frame_time: *frame_time,
                    looping: *looping,
                });
            }
            _ => {}
        }
    }

    context
}

fn render_animation_update(out: &mut String, animation: &AnimationState) {
    if animation.frame_time == 0 || animation.frame_tiles.len() <= 1 {
        return;
    }

    let last_frame = animation.frame_tiles.len() - 1;
    out.push_str(&format!(
        "        if (++{name}_timer >= {delay}) {{\n",
        name = animation.state_name,
        delay = animation.frame_time
    ));
    out.push_str(&format!(
        "            {}_timer = 0;\n",
        animation.state_name
    ));
    out.push_str(&format!(
        "            if ({}_cursor < {}) {{\n",
        animation.state_name, last_frame
    ));
    out.push_str(&format!(
        "                {}_cursor++;\n",
        animation.state_name
    ));
    out.push_str(&format!(
        "                oamSet({index}, {x}, {y}, {priority}, 0, 0, {name}_tiles[{name}_cursor], {palette});\n",
        index = animation.sprite_index,
        x = animation.x_var,
        y = animation.y_var,
        priority = animation.priority,
        name = animation.state_name,
        palette = animation.palette_slot
    ));
    out.push_str("            }");
    if animation.looping {
        out.push_str(" else {\n");
        out.push_str(&format!(
            "                {}_cursor = 0;\n",
            animation.state_name
        ));
        out.push_str(&format!(
            "                oamSet({index}, {x}, {y}, {priority}, 0, 0, {name}_tiles[{name}_cursor], {palette});\n",
            index = animation.sprite_index,
            x = animation.x_var,
            y = animation.y_var,
            priority = animation.priority,
            name = animation.state_name,
            palette = animation.palette_slot
        ));
        out.push_str("            }\n");
    } else {
        out.push('\n');
    }
    out.push_str("        }\n");
}

fn render_scroll_tilemap(out: &mut String, layer: &str, dx: i32, dy: i32) {
    let layer_id = scroll_layer_id(layer);
    let bg = snes_bg_number(layer);
    out.push_str(&format!(
        "        tm_scroll_x_{layer_id} += {dx};\n",
        layer_id = layer_id,
        dx = dx
    ));
    out.push_str(&format!(
        "        tm_scroll_y_{layer_id} += {dy};\n",
        layer_id = layer_id,
        dy = dy
    ));
    out.push_str(&format!(
        "        bgSetScroll({bg}, tm_scroll_x_{layer_id}, tm_scroll_y_{layer_id});\n",
        bg = bg,
        layer_id = layer_id
    ));
}

fn render_move_camera(out: &mut String, target: &str, x: i32, y: i32, bg_numbers: &[u8]) {
    let (x_expr, y_expr) = if target.starts_with("spr_") {
        (
            format!("{}_x + {}", target, x),
            format!("{}_y + {}", target, y),
        )
    } else {
        (x.to_string(), y.to_string())
    };

    for bg_number in bg_numbers {
        out.push_str(&format!(
            "        bgSetScroll({}, {}, {});\n",
            bg_number, x_expr, y_expr
        ));
    }
}

fn render_aabb_helper() -> &'static str {
    "static u8 retro_aabb_intersects(s16 left_x, s16 left_y, u16 left_w, u16 left_h, s16 right_x, s16 right_y, u16 right_w, u16 right_h)\n\
{\n\
    return left_x < (right_x + (s16)right_w)\n\
        && (left_x + (s16)left_w) > right_x\n\
        && left_y < (right_y + (s16)right_h)\n\
        && (left_y + (s16)left_h) > right_y;\n\
}\n"
}

fn render_apply_physics(out: &mut String, context: &SnesContext, physics: &PhysicsApplication) {
    let Some(spawn) = context
        .spawns
        .iter()
        .find(|spawn| spawn.var_name == physics.var_name)
    else {
        return;
    };

    let x_var = format!("{}_x", physics.var_name);
    let y_var = format!("{}_y", physics.var_name);

    if physics.gravity {
        out.push_str(&format!(
            "        {var_name}_vel_y += {gravity_strength};\n",
            var_name = physics.var_name,
            gravity_strength = physics.gravity_strength
        ));
    }
    out.push_str(&format!(
        "        if ({var_name}_vel_x > {max_velocity_x}) {var_name}_vel_x = {max_velocity_x};\n",
        var_name = physics.var_name,
        max_velocity_x = physics.max_velocity_x
    ));
    out.push_str(&format!(
        "        if ({var_name}_vel_x < -{max_velocity_x}) {var_name}_vel_x = -{max_velocity_x};\n",
        var_name = physics.var_name,
        max_velocity_x = physics.max_velocity_x
    ));
    out.push_str(&format!(
        "        if ({var_name}_vel_y > {max_velocity_y}) {var_name}_vel_y = {max_velocity_y};\n",
        var_name = physics.var_name,
        max_velocity_y = physics.max_velocity_y
    ));
    out.push_str(&format!(
        "        if ({var_name}_vel_y < -{max_velocity_y}) {var_name}_vel_y = -{max_velocity_y};\n",
        var_name = physics.var_name,
        max_velocity_y = physics.max_velocity_y
    ));

    if physics.friction > 0 {
        out.push_str(&format!(
            "        if ({var_name}_vel_x > 0) {{ {var_name}_vel_x -= {friction}; if ({var_name}_vel_x < 0) {var_name}_vel_x = 0; }}\n",
            var_name = physics.var_name,
            friction = physics.friction
        ));
        out.push_str(&format!(
            "        if ({var_name}_vel_x < 0) {{ {var_name}_vel_x += {friction}; if ({var_name}_vel_x > 0) {var_name}_vel_x = 0; }}\n",
            var_name = physics.var_name,
            friction = physics.friction
        ));
    }

    out.push_str(&format!(
        "        {x_var} += ({var_name}_vel_x / 16);\n",
        x_var = x_var,
        var_name = physics.var_name
    ));
    out.push_str(&format!(
        "        {y_var} += ({var_name}_vel_y / 16);\n",
        y_var = y_var,
        var_name = physics.var_name
    ));

    if physics.bounce > 0 {
        out.push_str(&format!(
            "        if ({x_var} < 0) {{ {x_var} = 0; {var_name}_vel_x = -(({var_name}_vel_x * {bounce}) / 100); }}\n",
            x_var = x_var,
            var_name = physics.var_name,
            bounce = physics.bounce
        ));
        out.push_str(&format!(
            "        if ({y_var} < 0) {{ {y_var} = 0; {var_name}_vel_y = -(({var_name}_vel_y * {bounce}) / 100); }}\n",
            y_var = y_var,
            var_name = physics.var_name,
            bounce = physics.bounce
        ));
    }

    out.push_str(&format!(
        "        oamSet({index}, {x_var}, {y_var}, {priority}, 0, 0, {tile}, {palette});\n",
        index = spawn.sprite_index,
        x_var = x_var,
        y_var = y_var,
        priority = spawn.priority,
        tile = current_tile_expression(context, &physics.var_name),
        palette = spawn.palette_slot
    ));
}

fn render_logic_scripts(
    out: &mut String,
    scripts: &[LogicScript],
    context: &SnesContext,
    indent: usize,
) {
    for script in scripts {
        if matches!(script.ops.first(), Some(LogicOp::HardwareEvent { .. })) {
            continue;
        }
        render_logic_ops(out, &script.ops, context, indent);
    }
}

fn sanitize_command_identifier(value: &str) -> String {
    let mut out = String::new();
    let mut previous_separator = false;
    for ch in value.chars() {
        if ch.is_ascii_alphanumeric() {
            out.push(ch.to_ascii_lowercase());
            previous_separator = false;
        } else if !previous_separator {
            out.push('_');
            previous_separator = true;
        }
    }
    let sanitized = out.trim_matches('_').to_string();
    if sanitized.is_empty() {
        "command".to_string()
    } else {
        sanitized
    }
}

fn collect_input_commands(ast: &AstOutput) -> BTreeMap<String, InputCommandSpec> {
    let mut commands = BTreeMap::new();
    for script in &ast.logic_scripts {
        collect_input_commands_from_ops(&script.ops, &mut commands);
    }
    commands
}

fn collect_input_commands_from_ops(ops: &[LogicOp], commands: &mut BTreeMap<String, InputCommandSpec>) {
    for op in ops {
        match op {
            LogicOp::ConditionBool { condition, if_true, if_false } => {
                collect_input_command_from_bool(condition, commands);
                collect_input_commands_from_ops(if_true, commands);
                collect_input_commands_from_ops(if_false, commands);
            }
            LogicOp::WhileLoop { condition, body, done } => {
                collect_input_command_from_bool(condition, commands);
                collect_input_commands_from_ops(body, commands);
                collect_input_commands_from_ops(done, commands);
            }
            LogicOp::ConditionOverlap { if_true, if_false, .. } => {
                collect_input_commands_from_ops(if_true, commands);
                collect_input_commands_from_ops(if_false, commands);
            }
            LogicOp::ForLoop { body, done, .. } => {
                collect_input_commands_from_ops(body, commands);
                collect_input_commands_from_ops(done, commands);
            }
            LogicOp::TimelineSequence { slots, .. } => {
                for slot in slots {
                    collect_input_commands_from_ops(&slot.actions, commands);
                }
            }
            LogicOp::HardwareBudgetCheck { if_ok, if_warn, .. } => {
                collect_input_commands_from_ops(if_ok, commands);
                collect_input_commands_from_ops(if_warn, commands);
            }
            LogicOp::StateMachine { states, .. } => {
                for state in states {
                    collect_input_commands_from_ops(&state.body, commands);
                    for transition in &state.transitions {
                        collect_input_command_from_bool(&transition.condition, commands);
                        collect_input_commands_from_ops(&transition.if_matched, commands);
                        collect_input_commands_from_ops(&transition.if_unmatched, commands);
                    }
                }
            }
            LogicOp::HardwareEvent { ops, .. } => collect_input_commands_from_ops(ops, commands),
            _ => {}
        }
    }
}

fn collect_input_command_from_bool(expr: &LogicBoolExpr, commands: &mut BTreeMap<String, InputCommandSpec>) {
    match expr {
        LogicBoolExpr::InputCommand {
            command_id,
            notation,
            unsupported_tokens,
            ..
        } => {
            commands.entry(command_id.clone()).or_insert_with(|| InputCommandSpec {
                id: command_id.clone(),
                notation: notation.clone(),
                unsupported_tokens: unsupported_tokens.clone(),
            });
        }
        LogicBoolExpr::Not(value) => collect_input_command_from_bool(value, commands),
        LogicBoolExpr::And { left, right, .. } => {
            collect_input_command_from_bool(left, commands);
            collect_input_command_from_bool(right, commands);
        }
        _ => {}
    }
}

fn render_input_command_runtime(out: &mut String, commands: &BTreeMap<String, InputCommandSpec>) {
    out.push_str(
        "typedef struct { u8 direction; u16 buttons; } RdsInputCommandStep;\n\
         #define RDS_INPUT_HISTORY 32\n\
         static u8 rds_input_directions[RDS_INPUT_HISTORY];\n\
         static u16 rds_input_buttons[RDS_INPUT_HISTORY];\n\
         static u8 rds_input_cursor = 0;\n\
         static u8 rds_input_direction_from_buttons(u16 buttons) {\n\
             u8 left = (buttons & KEY_LEFT) != 0;\n\
             u8 right = (buttons & KEY_RIGHT) != 0;\n\
             u8 up = (buttons & KEY_UP) != 0;\n\
             u8 down = (buttons & KEY_DOWN) != 0;\n\
             if (down && left) return 1;\n\
             if (down && right) return 3;\n\
             if (up && left) return 7;\n\
             if (up && right) return 9;\n\
             if (down) return 2;\n\
             if (left) return 4;\n\
             if (right) return 6;\n\
             if (up) return 8;\n\
             return 5;\n\
         }\n\
         static void rds_input_push_frame(u16 buttons) {\n\
             rds_input_directions[rds_input_cursor] = rds_input_direction_from_buttons(buttons);\n\
             rds_input_buttons[rds_input_cursor] = buttons;\n\
             rds_input_cursor = (rds_input_cursor + 1) % RDS_INPUT_HISTORY;\n\
         }\n\
         static u8 rds_input_step_matches(const RdsInputCommandStep* step, u8 index) {\n\
             u8 direction_ok = (step->direction == 0) || (step->direction == rds_input_directions[index]);\n\
             u8 buttons_ok = (step->buttons == 0) || ((rds_input_buttons[index] & step->buttons) == step->buttons);\n\
             return direction_ok && buttons_ok;\n\
         }\n\
         static u8 rds_input_match_command(const RdsInputCommandStep* steps, u8 step_count, u8 max_frames) {\n\
             u8 matched = step_count;\n\
             u8 scanned = 0;\n\
             u8 cursor = rds_input_cursor;\n\
             if (step_count == 0) return 0;\n\
             while ((scanned < max_frames) && (scanned < RDS_INPUT_HISTORY) && (matched > 0)) {\n\
                 cursor = (cursor + RDS_INPUT_HISTORY - 1) % RDS_INPUT_HISTORY;\n\
                 if (rds_input_step_matches(&steps[matched - 1], cursor)) matched--;\n\
                 scanned++;\n\
             }\n\
             return matched == 0;\n\
         }\n"
    );

    for spec in commands.values() {
        let parsed = parse_command_notation(&spec.notation);
        let unsupported = if spec.unsupported_tokens.is_empty() {
            parsed.unsupported_tokens
        } else {
            spec.unsupported_tokens.clone()
        };
        if !unsupported.is_empty() {
            out.push_str(&format!(
                "#error \"Unsupported input_command tokens for {}: {}\"\n",
                spec.id,
                unsupported.join(", ")
            ));
            continue;
        }
        out.push_str(&format!(
            "static const RdsInputCommandStep rds_cmd_{}_steps[] = {{\n",
            sanitize_command_identifier(&spec.id)
        ));
        for step in parsed.steps {
            let button_mask = if step.buttons.is_empty() {
                "0".to_string()
            } else {
                step.buttons
                    .iter()
                    .map(|button| snes_button_mask(button).unwrap_or("KEY_Y").to_string())
                    .collect::<Vec<_>>()
                    .join(" | ")
            };
            out.push_str(&format!(
                "    {{ {}, {} }},\n",
                step.direction.unwrap_or(0),
                button_mask
            ));
        }
        out.push_str("};\n");
    }
}

fn collect_hardware_event_scripts(ast: &AstOutput) -> Vec<(HardwareEventKind, Vec<LogicOp>)> {
    ast.logic_scripts
        .iter()
        .filter_map(|script| match script.ops.first() {
            Some(LogicOp::HardwareEvent { event, ops }) => Some((*event, ops.clone())),
            _ => None,
        })
        .collect()
}

fn snes_event_registration(event: HardwareEventKind) -> String {
    match event {
        HardwareEventKind::VBlank => "    nmiSet(retro_on_vblank);\n".to_string(),
        HardwareEventKind::HBlank => {
            "    irqInit(); irqSet(IRQ_HBLANK, retro_on_hblank);\n".to_string()
        }
        HardwareEventKind::DmaDone => "    dmaSetCallback(retro_on_dma_done);\n".to_string(),
    }
}

fn snes_event_handler_name(event: HardwareEventKind) -> &'static str {
    match event {
        HardwareEventKind::VBlank => "retro_on_vblank",
        HardwareEventKind::HBlank => "retro_on_hblank",
        HardwareEventKind::DmaDone => "retro_on_dma_done",
    }
}

fn render_snes_hardware_event_handlers(
    out: &mut String,
    context: &SnesContext,
    scripts: &[(HardwareEventKind, Vec<LogicOp>)],
) {
    for (event, ops) in scripts {
        out.push_str(&format!(
            "static void {}(void) {{\n",
            snes_event_handler_name(*event)
        ));
        render_logic_ops(out, ops, context, 4);
        out.push_str("}\n");
    }
}

fn render_logic_ops(out: &mut String, ops: &[LogicOp], context: &SnesContext, indent: usize) {
    let indent_str = " ".repeat(indent);
    let nested_indent = indent + 4;

    for op in ops {
        match op {
            LogicOp::MoveSprite { target_var, dx, dy } => {
                let Some(spawn) = context
                    .spawns
                    .iter()
                    .find(|spawn| spawn.var_name == *target_var)
                else {
                    continue;
                };

                out.push_str(&format!(
                    "{indent}{target}_x += {dx};\n",
                    indent = indent_str,
                    target = target_var,
                    dx = dx
                ));
                out.push_str(&format!(
                    "{indent}{target}_y += {dy};\n",
                    indent = indent_str,
                    target = target_var,
                    dy = dy
                ));
                out.push_str(&format!(
                    "{indent}oamSet({index}, {target}_x, {target}_y, {priority}, 0, 0, {tile}, {palette});\n",
                    indent = indent_str,
                    index = spawn.sprite_index,
                    target = target_var,
                    priority = spawn.priority,
                    tile = current_tile_expression(context, target_var),
                    palette = spawn.palette_slot
                ));
            }
            LogicOp::SetSpritePosition { target_var, x, y } => {
                let x_expr = render_math_expr(x);
                let y_expr = render_math_expr(y);
                out.push_str(&format!(
                    "{indent}{target}_x = {x};\n",
                    indent = indent_str,
                    target = target_var,
                    x = x_expr
                ));
                out.push_str(&format!(
                    "{indent}{target}_y = {y};\n",
                    indent = indent_str,
                    target = target_var,
                    y = y_expr
                ));
                if let Some(spawn) = context
                    .spawns
                    .iter()
                    .find(|spawn| spawn.var_name == *target_var)
                {
                    out.push_str(&format!(
                        "{indent}oamSet({index}, {target}_x, {target}_y, {priority}, 0, 0, {tile}, {palette});\n",
                        indent = indent_str,
                        index = spawn.sprite_index,
                        target = target_var,
                        priority = spawn.priority,
                        tile = current_tile_expression(context, target_var),
                        palette = spawn.palette_slot
                    ));
                }
            }
            LogicOp::SetVelocity {
                target_name,
                vx,
                vy,
            } => {
                out.push_str(&format!(
                    "{indent}logic_var_{target}_vx = {vx};\n",
                    indent = indent_str,
                    target = target_name,
                    vx = render_math_expr(vx)
                ));
                out.push_str(&format!(
                    "{indent}logic_var_{target}_vy = {vy};\n",
                    indent = indent_str,
                    target = target_name,
                    vy = render_math_expr(vy)
                ));
            }
            LogicOp::SetAnimationState {
                target_var,
                anim_index,
            } => {
                out.push_str(&format!(
                    "{indent}// Animation state for {target}: {anim_index}\n",
                    indent = indent_str,
                    target = target_var,
                    anim_index = anim_index
                ));
            }
            LogicOp::SetTile { layer, tile, x, y } => {
                out.push_str(&format!(
                    "{indent}// Set tile on {layer}: tile {tile} at {x},{y}\n",
                    indent = indent_str,
                    layer = layer,
                    tile = render_math_expr(tile),
                    x = render_math_expr(x),
                    y = render_math_expr(y)
                ));
            }
            LogicOp::CameraFollow {
                target_var,
                offset_x,
                offset_y,
            } => {
                out.push_str(&format!(
                    "{indent}bgSetScroll(0, {}, {});\n",
                    camera_axis_expr(target_var, "x", *offset_x),
                    camera_axis_expr(target_var, "y", *offset_y),
                    indent = indent_str
                ));
            }
            LogicOp::ShowSprite { target_var, x, y } => {
                out.push_str(&format!(
                    "{indent}{target}_x = {x}; {target}_y = {y};\n",
                    indent = indent_str,
                    target = target_var,
                    x = render_math_expr(x),
                    y = render_math_expr(y)
                ));
            }
            LogicOp::HideSprite { target_var } => {
                out.push_str(&format!(
                    "{indent}// Hide sprite {target}\n",
                    indent = indent_str,
                    target = target_var
                ));
            }
            LogicOp::ConditionOverlap {
                left,
                right,
                if_true,
                if_false,
            } => {
                out.push_str(&format!(
                    "{indent}if (retro_aabb_intersects({left_x}, {left_y}, {left_w}, {left_h}, {right_x}, {right_y}, {right_w}, {right_h})) {{\n",
                    indent = indent_str,
                    left_x = logic_x_expr(left),
                    left_y = logic_y_expr(left),
                    left_w = left.width,
                    left_h = left.height,
                    right_x = logic_x_expr(right),
                    right_y = logic_y_expr(right),
                    right_w = right.width,
                    right_h = right.height
                ));
                render_logic_ops(out, if_true, context, nested_indent);
                if if_false.is_empty() {
                    out.push_str(&format!("{}}}\n", indent_str));
                } else {
                    out.push_str(&format!("{}}} else {{\n", indent_str));
                    render_logic_ops(out, if_false, context, nested_indent);
                    out.push_str(&format!("{}}}\n", indent_str));
                }
            }
            LogicOp::ConditionBool {
                condition,
                if_true,
                if_false,
            } => {
                let condition_expr = render_bool_expr(out, condition, indent);
                out.push_str(&format!(
                    "{indent}if ({condition}) {{\n",
                    indent = indent_str,
                    condition = condition_expr
                ));
                render_logic_ops(out, if_true, context, nested_indent);
                if if_false.is_empty() {
                    out.push_str(&format!("{}}}\n", indent_str));
                } else {
                    out.push_str(&format!("{}}} else {{\n", indent_str));
                    render_logic_ops(out, if_false, context, nested_indent);
                    out.push_str(&format!("{}}}\n", indent_str));
                }
            }
            LogicOp::PlaySound { sfx } => {
                out.push_str(&format!(
                    "{indent}spcPlaySound(SFX_{sfx});\n",
                    indent = indent_str,
                    sfx = sfx.to_uppercase()
                ));
            }
            LogicOp::SetVar { var_name, value } => {
                let value_expr = render_math_expr(value);
                out.push_str(&format!(
                    "{indent}logic_var_{var_name} = {value_expr};\n",
                    indent = indent_str,
                    var_name = var_name,
                    value_expr = value_expr
                ));
            }
            LogicOp::WhileLoop {
                condition,
                body,
                done,
            } => {
                let condition_expr = render_bool_expr(out, condition, indent);
                out.push_str(&format!(
                    "{indent}while ({condition}) {{\n",
                    indent = indent_str,
                    condition = condition_expr
                ));
                render_logic_ops(out, body, context, indent + 4);
                out.push_str(&format!("{indent}}}\n", indent = indent_str));
                render_logic_ops(out, done, context, indent);
            }
            LogicOp::ForLoop {
                loop_var,
                count,
                body,
                done,
            } => {
                let count_expr = render_math_expr(count);
                out.push_str(&format!(
                    "{indent}for (s16 {loop_var} = 0; {loop_var} < {count}; {loop_var}++) {{\n",
                    indent = indent_str,
                    loop_var = loop_var,
                    count = count_expr
                ));
                render_logic_ops(out, body, context, indent + 4);
                out.push_str(&format!("{indent}}}\n", indent = indent_str));
                render_logic_ops(out, done, context, indent);
            }
            LogicOp::TimelineSequence { counter_var, slots } => {
                render_timeline_sequence(out, context, counter_var, slots, indent);
            }
            LogicOp::HardwareBudgetCheck {
                vram_kb,
                sprites,
                scanline_sprites,
                if_ok,
                if_warn,
            } => {
                out.push_str(&format!(
                    "{indent}// Hardware budget check: VRAM {vram_kb}KB, sprites {sprites}, sprites/scanline {scanline_sprites}\n",
                    indent = indent_str,
                    vram_kb = vram_kb,
                    sprites = sprites,
                    scanline_sprites = scanline_sprites
                ));
                render_logic_ops(out, if_ok, context, indent);
                if !if_warn.is_empty() {
                    out.push_str(&format!(
                        "{indent}// Hardware budget warning branch\n",
                        indent = indent_str
                    ));
                    render_logic_ops(out, if_warn, context, indent);
                }
            }
            LogicOp::HardwareEvent { ops, .. } => {
                render_logic_ops(out, ops, context, indent);
            }
            LogicOp::StateMachine {
                machine_var,
                states,
            } => {
                render_fsm_states(out, context, machine_var, states, indent);
            }
        }
    }
}

fn render_timeline_sequence(
    out: &mut String,
    context: &SnesContext,
    counter_var: &str,
    slots: &[LogicTimelineSlot],
    indent: usize,
) {
    let indent_str = " ".repeat(indent);
    out.push_str(&format!(
        "{indent}logic_var_{counter}++;\n",
        indent = indent_str,
        counter = counter_var
    ));
    out.push_str(&format!(
        "{indent}switch (logic_var_{counter}) {{\n",
        indent = indent_str,
        counter = counter_var
    ));
    for slot in slots {
        out.push_str(&format!(
            "{indent}case {delay}:\n",
            indent = " ".repeat(indent + 4),
            delay = slot.delay_frames
        ));
        render_logic_ops(out, &slot.actions, context, indent + 8);
        out.push_str(&format!(
            "{indent}break;\n",
            indent = " ".repeat(indent + 8)
        ));
    }
    out.push_str(&format!(
        "{indent}default:\n",
        indent = " ".repeat(indent + 4)
    ));
    out.push_str(&format!(
        "{indent}break;\n",
        indent = " ".repeat(indent + 8)
    ));
    out.push_str(&format!("{indent}}}\n", indent = indent_str));
}

fn render_fsm_states(
    out: &mut String,
    context: &SnesContext,
    machine_var: &str,
    states: &[LogicFsmState],
    indent: usize,
) {
    let indent_str = " ".repeat(indent);

    for (index, state) in states.iter().enumerate() {
        let keyword = if index == 0 { "if" } else { "else if" };
        out.push_str(&format!(
            "{indent}{keyword} (logic_var_{machine_var} == {state_index}) {{\n",
            indent = indent_str,
            keyword = keyword,
            machine_var = machine_var,
            state_index = state.state_index
        ));
        render_logic_ops(out, &state.body, context, indent + 4);
        render_fsm_transitions(out, context, machine_var, &state.transitions, indent + 4);
        out.push_str(&format!("{indent}}}\n", indent = indent_str));
    }
}

fn render_fsm_transitions(
    out: &mut String,
    context: &SnesContext,
    machine_var: &str,
    transitions: &[LogicFsmTransition],
    indent: usize,
) {
    let indent_str = " ".repeat(indent);

    for (index, transition) in transitions.iter().enumerate() {
        let keyword = if index == 0 { "if" } else { "else if" };
        let condition_expr = render_bool_expr(out, &transition.condition, indent);
        out.push_str(&format!(
            "{indent}{keyword} ({condition}) {{\n",
            indent = indent_str,
            keyword = keyword,
            condition = condition_expr
        ));
        out.push_str(&format!(
            "{inner}logic_var_{machine_var} = {target};\n",
            inner = " ".repeat(indent + 4),
            machine_var = machine_var,
            target = transition.target_index
        ));
        render_logic_ops(out, &transition.if_matched, context, indent + 4);
        if !transition.if_unmatched.is_empty() {
            out.push_str(&format!("{indent}}} else {{\n", indent = indent_str));
            render_logic_ops(out, &transition.if_unmatched, context, indent + 4);
        }
        out.push_str(&format!("{indent}}}\n", indent = indent_str));
    }
}

fn render_bool_expr(out: &mut String, expr: &LogicBoolExpr, indent: usize) -> String {
    match expr {
        LogicBoolExpr::Literal(value) => {
            if *value { "1".to_string() } else { "0".to_string() }
        }
        LogicBoolExpr::Input { button, .. } => format!("(padsCurrent(0) & {})", button),
        LogicBoolExpr::InputCommand {
            command_id,
            notation,
            max_frames,
            unsupported_tokens,
            ..
        } => {
            let parsed = parse_command_notation(notation);
            let unsupported = if unsupported_tokens.is_empty() {
                parsed.unsupported_tokens
            } else {
                unsupported_tokens.clone()
            };
            if !unsupported.is_empty() {
                out.push_str(&format!(
                    "{}#error \"Unsupported input_command tokens for {}: {}\"\n",
                    " ".repeat(indent),
                    command_id,
                    unsupported.join(", ")
                ));
                "0".to_string()
            } else {
                format!(
                    "rds_input_match_command(rds_cmd_{}_steps, {}, {})",
                    sanitize_command_identifier(command_id),
                    parsed.steps.len(),
                    max_frames
                )
            }
        }
        LogicBoolExpr::Overlap { left, right } => format!(
            "retro_aabb_intersects({left_x}, {left_y}, {left_w}, {left_h}, {right_x}, {right_y}, {right_w}, {right_h})",
            left_x = logic_x_expr(left),
            left_y = logic_y_expr(left),
            left_w = left.width,
            left_h = left.height,
            right_x = logic_x_expr(right),
            right_y = logic_y_expr(right),
            right_w = right.width,
            right_h = right.height
        ),
        LogicBoolExpr::Not(value) => format!("!({})", render_bool_expr(out, value, indent)),
        LogicBoolExpr::And {
            result_name,
            left,
            right,
        } => {
            let left_expr = render_bool_expr(out, left, indent);
            let right_expr = render_bool_expr(out, right, indent);
            let indent_str = " ".repeat(indent);
            out.push_str(&format!(
                "{indent}u8 {result_name} = ({left} && {right});\n",
                indent = indent_str,
                result_name = result_name,
                left = left_expr,
                right = right_expr
            ));
            result_name.clone()
        }
        LogicBoolExpr::Compare { op, left, right } => {
            let left_expr = render_math_expr(left);
            let right_expr = render_math_expr(right);
            let op_str = match op {
                CompareOp::Eq => "==",
                CompareOp::Neq => "!=",
                CompareOp::Gt => ">",
                CompareOp::Gte => ">=",
                CompareOp::Lt => "<",
                CompareOp::Lte => "<=",
            };
            format!("({left_expr} {op_str} {right_expr})")
        }
    }
}

fn render_math_expr(expr: &LogicMathExpr) -> String {
    match expr {
        LogicMathExpr::Literal(val) => val.to_string(),
        LogicMathExpr::Var(name) => format!("logic_var_{}", name),
        LogicMathExpr::Add(left, right) => {
            format!("({} + {})", render_math_expr(left), render_math_expr(right))
        }
        LogicMathExpr::Sub(left, right) => {
            format!("({} - {})", render_math_expr(left), render_math_expr(right))
        }
        LogicMathExpr::Mul(left, right) => {
            format!("({} * {})", render_math_expr(left), render_math_expr(right))
        }
        LogicMathExpr::Div(left, right) => {
            format!("({} / {})", render_math_expr(left), render_math_expr(right))
        }
    }
}

fn render_retrofx_frame(
    out: &mut String,
    parallax_layers: &[ParallaxLayerConfig],
    raster_lines: &[RasterLineConfig],
    indent: usize,
) {
    if parallax_layers.is_empty() && raster_lines.is_empty() {
        return;
    }

    let indent_str = " ".repeat(indent);
    for (index, layer) in parallax_layers.iter().enumerate() {
        out.push_str(&format!(
            "{indent}retro_parallax_offset_{index}_x += {speed_x};\n",
            indent = indent_str,
            index = index,
            speed_x = layer.speed_x
        ));
        out.push_str(&format!(
            "{indent}retro_parallax_offset_{index}_y += {speed_y};\n",
            indent = indent_str,
            index = index,
            speed_y = layer.speed_y
        ));
    }

    let mut boundaries = vec![0usize, 224usize];
    if !parallax_layers.is_empty() {
        for index in 0..parallax_layers.len() {
            boundaries.push((index * 224) / parallax_layers.len());
            boundaries.push(((index + 1) * 224) / parallax_layers.len());
        }
    }
    let mut raster_offsets = std::collections::BTreeMap::<usize, i32>::new();
    for line in raster_lines {
        let scanline = line.scanline.min(223) as usize;
        boundaries.push(scanline);
        boundaries.push((scanline + 1).min(224));
        *raster_offsets.entry(scanline).or_insert(0) += line.offset_x;
    }
    boundaries.sort_unstable();
    boundaries.dedup();

    let mut hdma_offset = 0usize;
    for segment in boundaries.windows(2) {
        let start = segment[0];
        let end = segment[1];
        if end <= start {
            continue;
        }

        let base_expr = if parallax_layers.is_empty() {
            None
        } else {
            let layer_index =
                ((start * parallax_layers.len()) / 224).min(parallax_layers.len() - 1);
            Some(format!("retro_parallax_offset_{}_x", layer_index))
        };
        let raster_delta = raster_offsets.get(&start).copied().unwrap_or_default();
        let value_expr = match (base_expr, raster_delta) {
            (Some(expr), 0) => expr,
            (Some(expr), delta) if delta > 0 => format!("({expr} + {delta})"),
            (Some(expr), delta) => format!("({expr} - {})", delta.abs()),
            (None, 0) => "0".to_string(),
            (None, delta) => delta.to_string(),
        };

        out.push_str(&format!(
            "{indent}HDMATable16[{offset}] = {line_count};\n",
            indent = indent_str,
            offset = hdma_offset,
            line_count = end - start
        ));
        out.push_str(&format!(
            "{indent}HDMATable16[{offset}] = (u8)(((u16)({value})) & 0xFF);\n",
            indent = indent_str,
            offset = hdma_offset + 1,
            value = value_expr
        ));
        out.push_str(&format!(
            "{indent}HDMATable16[{offset}] = (u8)((((u16)({value})) >> 8) & 0xFF);\n",
            indent = indent_str,
            offset = hdma_offset + 2,
            value = value_expr
        ));
        hdma_offset += 3;
    }
    out.push_str(&format!(
        "{indent}HDMATable16[{offset}] = 0;\n",
        indent = indent_str,
        offset = hdma_offset
    ));
    out.push_str(&format!(
        "{indent}setParallaxScrolling(0);\n",
        indent = indent_str
    ));
    if !parallax_layers.is_empty() {
        out.push_str(&format!(
            "{indent}bgSetScroll(0, retro_parallax_offset_0_x, retro_parallax_offset_0_y);\n",
            indent = indent_str
        ));
    }
}

